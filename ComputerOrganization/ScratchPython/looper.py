sum = 0
current = 1
while current <= 100:
    sum += current
    current += 1
print("Sum of integers 1 ... 100 is", sum)
print("Stopped")

sum = 0
current = 1
while current <= 100:
    sum += current * current
    current += 1
print("Sum of the square of integers 1 ... 100 is", sum)
print("Stopped")